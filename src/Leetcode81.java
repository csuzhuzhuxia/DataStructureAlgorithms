    public boolean search(int[] nums, int target) {
        if (nums.length==0)
            return false;
        int left = 0;
        int right = nums.length-1;
        while (left<right){
            while (left+1<right&&nums[right]==nums[right-1])
                right--;
            while (left+1<right&&nums[left]==nums[left+1])
                left++;
            int mid = (left+right)/2;
            if (nums[mid]<nums[right]){
                right = mid;
            }else {
                left = mid+1;
            }
        }

        if (left==0){
            return Binary(nums,target,0,nums.length-1);
        }else {
            if (target==nums[nums.length-1]){
                return true;
            }else if (target<nums[nums.length-1]){
                return Binary(nums,target,left,nums.length-1);
            }else {
                return Binary(nums,target,0,left-1);
            }
        }


    }

    private int binarySearch(int[] nums, int left, int right, int target) {

        while (left<=right){
            int mid = (left+right)/2;
            if (nums[mid]==target)
                return mid;
            else if (nums[mid]<target){
                left = mid+1;
            }else
                right = mid-1;
        }
        return -1;
    }

    private boolean Binary(int[] nums, int target, int start, int end) {
        while (start<=end){
            int mid = (start+end)/2;
            if (nums[mid]<target){
                start = mid+1;
            }else if (nums[mid]>target){
                end = mid-1;
            }else {
                return true;
            }
        }
        return false;
    }

